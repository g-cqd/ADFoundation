//  ADFCore
//
//  The Eisel-Lemire fast path for decimal -> binary `Double` conversion (Daniel Lemire, "Number Parsing
//  at a Gigabyte per Second", 2021), ported from Go's `strconv/eisel_lemire.go`. It extends the Clinger
//  fast path in `DecimalFloat.double`: when a scanner has a decimal significand (<= 19 digits, so it fits
//  a `UInt64`) and a base-ten exponent that Clinger rejects (significand > 2^53, or |exponent| > 22),
//  Eisel-Lemire computes the correctly-rounded `Double` with a single 64x128-bit multiply against a
//  precomputed power-of-ten table -- no `String`, no `Double(_:)` -- OR returns `nil` when it cannot
//  *prove* the rounding is correct, in which case the caller falls back to the standard library.
//
//  Contract: `eiselLemire` NEVER returns an incorrectly-rounded value. Whenever it returns a non-nil
//  result, that result is bit-identical to `Double(theExactDecimalString)`. It is a pure accelerator:
//  a bug could only make it return `nil` too often (slower) -- never wrong -- because every uncertain
//  case bails to the correctly-rounded stdlib parser. This is verified exhaustively in
//  `EiselLemireTests` by a differential oracle against `Double(_:)` over millions of inputs.
//
//  The 128-bit `detailedPowersOfTen` table (exp10 in [-348, 347], stored as flat (lo, hi) pairs) is
//  generated by exact big-integer arithmetic -- `W_q = floor(10^q * 2^(127 - floor(q*log2 10)))`,
//  normalized so bit 127 is set -- never hand-transcribed. The generator asserts
//  `floor(q*log2 10) == (217706*q) >> 16` for every q, matching the exponent the algorithm derives from
//  that fixed-point constant.
//

extension DecimalFloat {
    /// `significand * 10^exponent` as a correctly-rounded `Double`, or `nil` when the Eisel-Lemire
    /// algorithm cannot guarantee correct rounding (-> caller uses the stdlib parser). `negative` applies
    /// the sign. `significand` must be the exact decimal significand with no more than 19 digits (so it
    /// fits a `UInt64` without truncation); `exponent` is the net power of ten to apply.
    ///
    /// Not `@inline(__always)`: it is the *slow* path relative to Clinger, and inlining the table +
    /// 128-bit arithmetic into every number-scan call site would bloat them. Callers try
    /// ``double(significand:exponent:negative:)`` (Clinger) first, then this, then the stdlib parser.
    public static func eiselLemire(
        significand: UInt64, exponent exp10: Int, negative neg: Bool
    ) -> Double? {
        var man = significand

        // man == 0 is exactly +/-0 regardless of the exponent (0e100 == 0).
        if man == 0 {
            return neg ? -0.0 : 0.0
        }
        // Outside the table the value is a guaranteed under/overflow (-> 0 or +/-inf) or a subnormal the
        // table cannot resolve; defer to the stdlib parser, which handles all of those correctly.
        if exp10 < -348 || 347 < exp10 {
            return nil
        }

        // Normalize the significand so its high bit is set; fold the shift into the binary exponent.
        let clz = man.leadingZeroBitCount
        man <<= UInt64(clz)
        // Biased base-two exponent. `(217706*exp10) >> 16` is floor(exp10*log2 10) (the table is
        // normalized to match). The wraparound `&-` below is deliberate (mirrors Go's uint64 arithmetic):
        // an underflow makes `retExp2 &- 1` huge, caught by the subnormal guard.
        let biasExp = ((217706 * exp10) >> 16) + 64 + 1023
        var retExp2 = UInt64(bitPattern: Int64(biasExp)) &- UInt64(clz)

        // 64x128 multiply, high 128 bits: (xHi, xLo) ~= floor(man * W_q / 2^64).
        let idx = 2 * (exp10 + 348)
        var (xHi, xLo) = man.multipliedFullWidth(by: detailedPowersOfTen[idx + 1])

        // Wider approximation: when the truncated product sits on a rounding boundary (low 9 bits all 1)
        // and the low half is within `man` of overflowing, refine with the table's low word. If it is
        // *still* ambiguous after the full 128-bit product, bail.
        if xHi & 0x1FF == 0x1FF && xLo &+ man < man {
            let (yHi, yLo) = man.multipliedFullWidth(by: detailedPowersOfTen[idx])
            var mergedHi = xHi
            let mergedLo = xLo &+ yHi
            if mergedLo < xLo { mergedHi &+= 1 }
            if mergedHi & 0x1FF == 0x1FF && mergedLo &+ 1 == 0 && yLo &+ man < man {
                return nil
            }
            xHi = mergedHi
            xLo = mergedLo
        }

        // Extract 54 significand bits (leading-bit-position-dependent) and finalize the exponent.
        let msb = xHi >> 63
        var retMantissa = xHi >> (msb &+ 9)
        retExp2 &-= (1 ^ msb)

        // Exact-halfway ambiguity (round-to-even can't be decided from these 54 bits): bail.
        if xLo == 0 && xHi & 0x1FF == 0 && retMantissa & 3 == 1 {
            return nil
        }

        // Round-to-nearest-even from 54 to 53 bits; a carry out bumps the exponent.
        retMantissa &+= retMantissa & 1
        retMantissa >>= 1
        if retMantissa >> 53 > 0 {
            retMantissa >>= 1
            retExp2 &+= 1
        }

        // Subnormal or overflow (biased exponent not in [1, 0x7FE]) -> let the stdlib parser handle it.
        if retExp2 &- 1 >= 0x7FF - 1 {
            return nil
        }

        var retBits = (retExp2 << 52) | (retMantissa & 0x000F_FFFF_FFFF_FFFF)
        if neg { retBits |= 0x8000_0000_0000_0000 }
        return Double(bitPattern: retBits)
    }

